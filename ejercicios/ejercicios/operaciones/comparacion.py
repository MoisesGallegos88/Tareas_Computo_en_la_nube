def igual(a, b):
    return a == b  # Igual a

def difde(a, b):
    return a != b  # Diferente de

def mayor_que(a, b):
    return a > b  # Mayor que

def menor_que(a, b):
    return a < b  # Menor que

def mayor_igual(a, b):
    return a >= b  # Mayor o igual que

def menor_igual(a, b):
    return a <= b  # Menor o igual que


